package com.codewithjunaid;

public class SwitchCase {

	public static void main(String[] args) {
int tech=2;
switch(tech) {
case 1:
{
	System.out.println(" Hello Python ");
	break;
}
case 2:
{
	System.out.println(" Hello Java ");
break;
}
default:
{
	System.out.println(" Not Listed ");

}
	}

}
}
